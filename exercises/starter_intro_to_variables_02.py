length = 10
width = 11
height = 12
base_area = length * width
surface_area = 2 * (length * width) + 2 * (length * height) + 2 * (width * height)
volume = length * width * height
print('Base area of the cuboid: ' + str(base_area))
print('Surface area of the cuboid: ' + str(surface_area))
print('Volume of the cuboid: ' + str(volume))
