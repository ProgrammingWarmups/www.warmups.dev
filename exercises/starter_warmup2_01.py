def mean(numbers):
    # add the numbers together
    numbers_sum = sum(numbers)
    # count how many values are in numbers
    numbers_count = len(numbers)
    # compute the arithmetic mean
    arithmetic_mean = numbers_sum / numbers_count
    return arithmetic_mean

